package com.sksouk.recyclerview;

public class recyclerview_class {
    private String txttt;
    private String txtms1;
    private String txtms2;
    private String txtms3;

    public recyclerview_class() {
    }

    public recyclerview_class(String txttt, String txtms1, String txtms2, String txtms3) {
        this.txttt = txttt;
        this.txtms1 = txtms1;
        this.txtms2 = txtms2;
        this.txtms3 = txtms3;
    }

    public String getTxttt() {
        return txttt;
    }

    public void setTxttt(String txttt) {
        this.txttt = txttt;
    }

    public String getTxtms1() {
        return txtms1;
    }

    public void setTxtms1(String txtms1) {
        this.txtms1 = txtms1;
    }

    public String getTxtms2() {
        return txtms2;
    }

    public void setTxtms2(String txtms2) {
        this.txtms2 = txtms2;
    }

    public String getTxtms3() {
        return txtms3;
    }

    public void setTxtms3(String txtms3) {
        this.txtms3 = txtms3;
    }
}
